
package com.jubayir;

public class Person {
    private String name;
    private int age;

    public Person() {
        setName("A N Other");
        setAge(21);
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    
    
    
}
